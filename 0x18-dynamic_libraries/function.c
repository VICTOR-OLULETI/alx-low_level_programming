#include "main.h"

/**
 * add - addition 
 * @a: first num
 * @b: second num
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtraction
 * @a: first num
 * @b: second num
 * Return: a - b;
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplication
 * @a: first num
 * @b: second num
 * Return: multiplication of a and b
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - division
 * @a: first num
 * @b: second num
 * Return: value of a divided by b.
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - remainder of division between two numbers
 * @a: first num
 * @b: second num
 * Return: remainder of division between a and b
 */
int mod(int a, int b)
{
	return (a % b);
}
